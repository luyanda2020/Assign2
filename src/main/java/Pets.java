public class Pets {

   private String color;
   private int age;

   public Pets(){

   }
   public Pets(String color){

       this.color = color;

   }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
